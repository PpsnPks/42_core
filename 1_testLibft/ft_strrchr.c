char	*ft_strrchr(const char *s, int c)
{
	while (*s != 0 && *(s + 1) != 0)
		s++;
	while (*s != 0)
	{
		if (*s == (char) c)
			return ((char *)s);
		s--;
	}
	return (0);
}
